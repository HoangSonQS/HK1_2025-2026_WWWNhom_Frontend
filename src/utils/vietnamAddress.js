/**
 * Lấy danh sách tỉnh/thành phố
 */
export const getProvinces = () => {
    return Object.keys(vietnamAddresses).sort();
};

/**
 * Lấy danh sách quận/huyện theo tỉnh/thành phố
 */
export const getDistricts = (province) => {
    if (!province || !vietnamAddresses[province]) {
        return [];
    }
    return Object.keys(vietnamAddresses[province]).sort();
};

/**
 * Lấy danh sách phường/xã theo tỉnh/thành phố và quận/huyện
 */
export const getWards = (province, district) => {
    if (!province || !district || !vietnamAddresses[province] || !vietnamAddresses[province][district]) {
        return [];
    }
    return vietnamAddresses[province][district].sort();
};

